#include "libft.h"

int		ft_atoi(char *str)
{
	int		i;
	int		n;
	int		is_negativ;

	i = 0;
	n = 0;
	is_negativ = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' ||
			str[i] == '\v' || str[i] == '\r' || str[i] == '\f')
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		i++;
		is_negativ = 1;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + str[i] - '0';
		i++;
	}
	if (is_negativ == 1)
		return (-n);
	return (n);
}
